# Megjegyzés
# Regressziós diagnosztika
# 1. ábra: ha a modell megfelelõ, akkor a reziduumok a nulla körüli 
# véletlen eltérések
# 2. ábra: QQ-ábra a reziduumok normalitásának ellenõrzésére, a pontoknak
# az egyenes mentén kell elhelyezkedniük
# 3. ábra: ha nem látunk semmilyen tendenciát az ábrán, akkor feltételezhetjük
# a reziduumok szórásának állandóságát
# 4. ábra: torzító pontok (olyan pontok, melyek erõsen befolyásolják a
# regressziós paraméterek becslését) detektálása: azok a pontok problémásak,
# melyek az 1-es Cook-féle D-értéknek megfelelõ szaggatott vonalon kívül esnek
plot(model)


detach(input)
